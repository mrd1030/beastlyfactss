// Site-relative path for <img src>, or null if this fact has no dedicated photo yet.
export function imagePathFor(fact) {
  if (!fact) return null;
  return FACT_IMAGES[fact.id] || ANIMAL_IMAGES[fact.animal] || null;
}

// Absolute beastlyfacts.com URL for this fact's photo, for og:image/twitter:image
// tags (which require an absolute URL) - or null if this fact has no dedicated photo.
export function absoluteImageFor(fact) {
  const path = imagePathFor(fact);
  return path ? `https://beastlyfacts.com${path}` : null;
}
